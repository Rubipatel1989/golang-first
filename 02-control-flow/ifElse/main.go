package main

func main() {
	a := 100
	b := 200

	if a == b {
		println("A & B are equals")
	} else if a < b {
		println("A is less than B")
	} else if a > b {
		println("A is greater than B")
	} else {
		println("A & B are not equals")
	}


	a = 300
	b = 200

	if a == b {
		println("A & B are equals")
	} else if a < b {
		println("A is less than B")
	} else if a > b {
		println("A is greater than B")
	} else {
		println("A & B are not equals")
	}

}
